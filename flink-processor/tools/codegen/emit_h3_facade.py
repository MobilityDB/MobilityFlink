import re, os, sys, subprocess

HERE = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
FACADE = os.path.join(HERE, "src/main/java/org/mobilitydb/flink/meos")
INC = os.environ.get("MEOS_INCLUDE", "/home/esteban/src/MobilityDB/meos/include")
_SIG = re.compile(r'public static (\S+) ([a-z_][A-Za-z0-9_]*)\(([^)]*)\)')
_DECL = re.compile(r'^\s*extern\s+.+?\b([a-z][A-Za-z0-9_]*)\s*\(', re.M)


def jar_sigs(jar):
    out = subprocess.run(["javap", "-classpath", jar, "-p", "functions.GeneratedFunctions"],
                         capture_output=True, text=True).stdout
    sigs = {}
    for line in out.splitlines():
        m = _SIG.search(line)
        if m:
            sigs[m.group(2)] = (m.group(1),
                                [a.strip() for a in m.group(3).strip().split(",")] if m.group(3).strip() else [])
    return sigs


def method(name, sigs):
    ret, types = sigs[name]
    decl = ", ".join(f"{t} arg{i}" for i, t in enumerate(types))
    call = ", ".join(f"arg{i}" for i in range(len(types)))
    retkw = "" if ret == "void" else "return "
    return (f"\n    /** MEOS {{@code {name}}} — H3 / th3index family. */\n"
            f"    public static {ret} {name}({decl}) {{\n"
            f"        if (!MEOS_AVAILABLE) {{\n"
            f"            throw new UnsupportedOperationException(\n"
            f'                "{name} requires libmeos — set -Dmobilityflink.meos.enabled=true");\n'
            f"        }}\n"
            f"        {retkw}GeneratedFunctions.{name}({call});\n"
            f"    }}\n")


def emit_class(cls, names, sigs):
    body = "".join(method(n, sigs) for n in names)
    txt = (MEOS_LICENSE_BANNER + "\n\n" + f"package org.mobilitydb.flink.meos;\n\n"
           f"/* AUTO-GENERATED by tools/codegen/emit_h3_facade.py — do not edit by hand.\n"
           f" * H3 / th3index family facade over functions.GeneratedFunctions.\n */\n\n"
           f"import functions.GeneratedFunctions;\n"
           f"import jnr.ffi.Pointer;\n\n"
           f"public final class {cls} {{\n\n"
           f"    public static final boolean MEOS_AVAILABLE = MeosOpsRuntime.MEOS_AVAILABLE;\n\n"
           f"    private {cls}() {{ /* utility */ }}\n"
           f"{body}}}\n")
    open(os.path.join(FACADE, f"{cls}.java"), "w").write(txt)
    return len(names)


def main():
    jar = sys.argv[1] if len(sys.argv) > 1 else os.path.join(HERE, "jar/JMEOS.jar")
    sigs = jar_sigs(jar)
    public = set(_DECL.findall(open(os.path.join(INC, "meos_h3.h")).read()))
    facade = sorted(public & set(sigs))
    th3 = [n for n in facade if n.startswith("th3index")]
    free = [n for n in facade if not n.startswith("th3index")]
    a = emit_class("MeosOpsTh3index", th3, sigs)
    b = emit_class("MeosOpsFreeH3", free, sigs)
    print(f"MeosOpsTh3index: {a} methods; MeosOpsFreeH3: {b} methods")


if __name__ == "__main__":
    main()
